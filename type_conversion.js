// convert string to number

let money = "50"
money = parseInt(money)
money = Number(money)
console.log(money);
console.log(typeof money)

// convert number to string

let money = 50

money = money.toString()
money = String(money)
console.log(money)
console.log(typeof money);

// convert string to decimal number 

let money = "40.44"
money = parseFloat(money)
console.log(money);
console.log(typeof money)